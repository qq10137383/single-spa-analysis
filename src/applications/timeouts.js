// 生命周期函数执行警告时间，默认1s
const defaultWarningMillis = 1000;

// 全局的生命周期函数执行时间配置
const globalTimeoutConfig = {
  bootstrap: {
    // 执行超时时间，执行达到这个时间会在控制台输出错误(dieOnTimeout:false)或者返回执行失败(dieOnTimeout:true)
    // 无论是在控制台输出错误还是警告，都会继续执行生命周期函数直到执行结束，但是如果dieOnTimeout设置为true，
    // 则达到超时时间(millis)后就直接返回执行失败了。
    millis: 4000,
    dieOnTimeout: false,
    // 警告时间，执行达到这个时间会在控制台输出警告信息
    warningMillis: defaultWarningMillis,
  },
  mount: {
    millis: 3000,
    dieOnTimeout: false,
    warningMillis: defaultWarningMillis,
  },
  unmount: {
    millis: 3000,
    dieOnTimeout: false,
    warningMillis: defaultWarningMillis,
  },
  unload: {
    millis: 3000,
    dieOnTimeout: false,
    warningMillis: defaultWarningMillis,
  },
  update: {
    millis: 3000,
    dieOnTimeout: false,
    warningMillis: defaultWarningMillis,
  },
};

export function setBootstrapMaxTime(time, dieOnTimeout, warningMillis) {
  if (typeof time !== "number" || time <= 0) {
    throw Error(
      formatErrorMessage(
        16,
        __DEV__ &&
        `bootstrap max time must be a positive integer number of milliseconds`
      )
    );
  }

  globalTimeoutConfig.bootstrap = {
    millis: time,
    dieOnTimeout,
    warningMillis: warningMillis || defaultWarningMillis,
  };
}

